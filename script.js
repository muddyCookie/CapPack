const releaseOrder = Object.keys(downloadsData);
const latestRelease = releaseOrder[0];

function setupMenu() {
  const toggle = document.querySelector("[data-menu-toggle]");
  const menu = document.querySelector("[data-menu]");
  if (!toggle || !menu) return;

  toggle.addEventListener("click", () => {
    menu.classList.toggle("open");
  });
}

function setupSlideshow() {
  const slideshow = document.querySelector("[data-slideshow]");
  if (!slideshow) return;

  const slides = Array.from(slideshow.querySelectorAll(".slide"));
  if (slides.length < 2) return;

  let idx = 0;
  window.setInterval(() => {
    slides[idx].classList.remove("active");
    idx = (idx + 1) % slides.length;
    slides[idx].classList.add("active");
  }, 3000);
}

function releaseMarkup(release, links, showTitle = true) {
  const chips = Object.entries(links)
    .map(
      ([version, url]) =>
        `<a class="download-chip" href="${url}">${version}</a>`
    )
    .join("");
  const title = showTitle ? `<h2 class="release-heading">${release}</h2>` : "";

  return `
    <div class="release-group">
      ${title}
      <div class="chip-wrap">${chips}</div>
    </div>
  `;
}

function releaseActionsMarkup(release, includeLatestExtras) {
  const githubLink = includeLatestExtras
    ? "https://github.com/CapPack/the-captainsparklez-texture-pack/releases/latest"
    : `https://github.com/CapPack/the-captainsparklez-texture-pack/releases/tag/${release}`;
  const latestExtras = includeLatestExtras
    ? `
      <a href="https://www.curseforge.com/minecraft/texture-packs/the-captainsparklez-texture-pack" target="_blank" rel="noopener noreferrer">
        <img src="src/assets/DlOnCurseforge.png" alt="Download on CurseForge" />
      </a>
      <a href="https://modrinth.com/resourcepack/the-captainsparklez-texture-pack" target="_blank" rel="noopener noreferrer">
        <img src="src/assets/DlOnModrinth.png" alt="Download on Modrinth" />
      </a>
      <a href="https://www.planetminecraft.com/texture-pack/the-captainsparklez-texture-pack/" target="_blank" rel="noopener noreferrer">
        <img src="src/assets/DlOnPlanetMinecraft.png" alt="Download on Planet Minecraft" />
      </a>
    `
    : "";

  return `
    <div class="platform-row release-actions">
      <a href="${githubLink}" target="_blank" rel="noopener noreferrer">
        <img src="src/assets/DlOnGitHub.png" alt="Download ${release} on GitHub" />
      </a>
      ${latestExtras}
    </div>
  `;
}

function setupDownloadsPage() {
  const latestView = document.getElementById("latest-release-view");
  const allView = document.getElementById("all-releases-view");
  if (!latestView || !allView) return;

  latestView.innerHTML = `
    <p class="release-heading">Current latest release: ${latestRelease}</p>
    ${releaseMarkup(latestRelease, downloadsData[latestRelease], false)}
    <div class="platform-row">
      <a href="https://github.com/CapPack/the-captainsparklez-texture-pack/releases/latest" target="_blank" rel="noopener noreferrer">
        <img src="src/assets/DlOnGitHub.png" alt="Download on GitHub" />
      </a>
      <a href="https://www.curseforge.com/minecraft/texture-packs/the-captainsparklez-texture-pack" target="_blank" rel="noopener noreferrer">
        <img src="src/assets/DlOnCurseforge.png" alt="Download on CurseForge" />
      </a>
      <a href="https://modrinth.com/resourcepack/the-captainsparklez-texture-pack" target="_blank" rel="noopener noreferrer">
        <img src="src/assets/DlOnModrinth.png" alt="Download on Modrinth" />
      </a>
      <a href="https://www.planetminecraft.com/texture-pack/the-captainsparklez-texture-pack/" target="_blank" rel="noopener noreferrer">
        <img src="src/assets/DlOnPlanetMinecraft.png" alt="Download on Planet Minecraft" />
      </a>
    </div>
  `;

  allView.innerHTML = releaseOrder
    .map((release) => {
      return `${releaseMarkup(release, downloadsData[release])}${releaseActionsMarkup(release, release === latestRelease)}`;
    })
    .join("");

  const modeButtons = Array.from(document.querySelectorAll(".mode-button"));
  modeButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const isLatest = button.dataset.mode === "latest";
      modeButtons.forEach((candidate) => candidate.classList.remove("active"));
      button.classList.add("active");
      latestView.classList.toggle("hidden", !isLatest);
      allView.classList.toggle("hidden", isLatest);
    });
  });
}

